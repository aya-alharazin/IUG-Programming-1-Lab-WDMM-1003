'''
Type casting is the process of converting a value from one data type to another.
There are TWO types of type casting:
Implicit Type Casting (Automatic) - Python does it for you
Explicit Type Casting (Manual) - You do it yourself using functions
1️⃣ Implicit Type Casting (Type Promotion)
int (+,*) float → float
2️⃣ Explicit Type Casting (Type Conversion)
int(), float(), str()
'''
